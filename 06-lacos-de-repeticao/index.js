/*
Laços em Javascript (ou loops) são uma maneira eficaz de se trabalhar com estruturas de repetições baseadas em estruturas condicionais dentro da linguagem, ao invés de repetir milhares de vezes determinado código, você pode utilizar os laços para repetir qualquer coisa até que qualquer outra coisa aconteça.

- for: Um laço for é repetido até que a condição especificada seja falsa.

- while: Uma declaração while executa suas instruções, desde que uma condição especificada seja avaliada como verdadeira.

- for of: cria uma laço com objetos interativos, executando uma iteração para o valor de cada propriedade distinta.

- for in: executa iterações a partir de uma variável específica, percorrendo todas as propriedades de um objeto.

- break: use break para terminar laços.

- continue: A declaração continue pode ser usada para reiniciar uma instrução.
*/